// Toggle info
function toggle(titleElement) {
    let text = document.querySelector('.text');
    let isVisible = text.style.display === 'block';

    if (isVisible) {
        text.style.display = 'none'; // Hide the text
    } else {
        text.style.display = 'block'; // Show the text
    }
}
